#Typecasting is the process of converting a value from one data
#type to another.
#In Python, you can use built-in functions to perform typecasting.
#functions 
int() # converts a value to an integer
float() # converts a value to a float
str() # converts a value to a string
#many more functions available in python for typecasting.

#Example of typecasting
#Converting a string to an integer
num_str = "123"
num_int = int(num_str)
print(num_int) # Output: 123
#Converting a float to an integer
num_float = 3.14
num_int = int(num_float)
print(num_int) # Output: 3
#Converting an integer to a string
num_int = 456
num_str = str(num_int)
print(num_str) # Output: "456"
#Typecasting can be useful when you need to perform operations that require 
# a specific data type.
