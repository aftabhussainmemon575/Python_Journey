
nums = [2,5,7,4,8,9]
nums.append (10)   # .append(value) => add one element at the end
print (nums)

nums.insert(1,56)  # .insert(idx, val) #insert element at idx
print (nums)

nums.sort()        # .sort() => Arranging nums in increasing order 
print(nums) 

nums.sort(reverse=True)  #sorted reverse order
print (nums)


nums.reverse()     # .reverse() => unsorted Reverse order
print(nums)




