# Q2. Take two numbers as input from the user and print their sum, difference, product, and quotient.

a = int(input("First number: "))
b = int(input("Second number: "))
sum = (a+b)
print ("Sum of", a, "and", b, "is = ", sum )
difference = (a-b)
print ("Difference of", a, "and", b, "is = ", difference)
product = (a*b)
print ("Product of", a, "and", b, "is = ", product)
quotient = (a/b)
quotient = int(quotient)
print ("Quotient of", a, "and", b, "is = ", quotient)